package access_modifiers;

public class PublicClassMemberMod {
	/*
	 * If a member is marked as public it's accessible in all java code
	 * 
	 */
	public void publicClassMemberMethod() {
		System.out.println("Hello! I am a class member with public modifier"
				+ "\n"
				+ "I am accessible in all code in this project.");
	}
}
